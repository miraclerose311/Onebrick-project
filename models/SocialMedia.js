const mongoose = require("mongoose");

const SocialMediaSchema = new mongoose.Schema({
  mediaType: {
    type: String,
    required: true,
  },
  description: {
    type: String,
  },
  image: {
    type: String,
    required: true,
  },
  url: {
    type: String,
    required: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

module.exports = mongoose.model("socialmedia", SocialMediaSchema);
